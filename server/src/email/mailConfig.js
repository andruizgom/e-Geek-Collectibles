require("dotenv").config();
const nodemailer = require("nodemailer");
const { MAILS_KEY, MAIL } = process.env;

const transporter = nodemailer.createTransport({
  host: "smtp.gmail.com",
  port: 465,
  secure: true,
  auth: {
    user: MAIL,
    pass: MAILS_KEY,
  },
});

transporter.verify().then(() => {
  console.log("Ready for send emails");
});

module.exports = {
  transporter,
};
